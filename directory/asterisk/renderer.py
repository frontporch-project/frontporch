import os
from pathlib import Path
from tempfile import NamedTemporaryFile


GENERATED_HEADER = "; GENERATED BY FRONTPORCH\n; DO NOT EDIT BY HAND\n"
PJSIP_FILENAME = "pjsip_frontporch.conf"
EXTENSIONS_FILENAME = "extensions_frontporch.conf"


class AsteriskConfigRenderer:
    def render_pjsip(self, configuration):
        lines = [GENERATED_HEADER.rstrip(), ""]

        for endpoint in configuration.endpoints:
            lines.extend(
                [
                    f"[{endpoint.endpoint_name}](endpoint-basic)",
                    f"context={endpoint.context_name}",
                    f"auth={endpoint.auth_name}",
                    f"aors={endpoint.aor_name}",
                    f'callerid="{endpoint.owner_display_name}" <{endpoint.extension}>',
                    "",
                    f"[{endpoint.auth_name}](auth-userpass)",
                    f"username={endpoint.username}",
                    f"password={endpoint.secret}",
                    "",
                    f"[{endpoint.aor_name}](aor-single-reg)",
                    "",
                ]
            )

        return "\n".join(lines).rstrip() + "\n"

    def render_extensions(self, configuration):
        rules_by_context = {}
        for rule in configuration.dialplan_rules:
            rules_by_context.setdefault(rule.source_endpoint.context_name, []).append(rule)
        external_rules_by_context = {}
        for rule in configuration.external_dialplan_rules:
            external_rules_by_context.setdefault(
                rule.source_endpoint.context_name,
                [],
            ).append(rule)
        shortcut_rules_by_context = {}
        for rule in configuration.shortcut_rules:
            shortcut_rules_by_context.setdefault(
                rule.source_endpoint.context_name,
                [],
            ).append(rule)

        lines = [GENERATED_HEADER.rstrip(), ""]

        for endpoint in configuration.endpoints:
            lines.append(f"[{endpoint.context_name}]")
            lines.append("include => frontporch-diagnostics")
            lines.append("")
            lines.extend(
                [
                    "exten => 100,1,Goto(frontporch-diagnostics,100,1)",
                    "",
                ]
            )
            for rule in shortcut_rules_by_context.get(endpoint.context_name, []):
                lines.extend(self._render_shortcut_rule(rule))
            for rule in rules_by_context.get(endpoint.context_name, []):
                target = rule.target_endpoint
                lines.extend(
                    self._render_call_lines(
                        f"exten => {rule.dialed_extension},1,",
                        source_endpoint=rule.source_endpoint,
                        target_endpoint=target,
                    )
                )
            for rule in external_rules_by_context.get(endpoint.context_name, []):
                lines.extend(
                    self._render_call_lines(
                        f"exten => {rule.dialed_extension},1,",
                        source_endpoint=rule.source_endpoint,
                        dial_target=f"PJSIP/{rule.outbound_number}@voipms-endpoint",
                    )
                )
            lines.extend(
                [
                    "exten => _X!,1,Hangup(21)",
                    "",
                ]
            )

        lines.extend(self._render_blackout_context())
        lines.extend(self._render_public_inbound_context(configuration))

        return "\n".join(lines).rstrip() + "\n"

    def _render_shortcut_rule(self, rule):
        if rule.is_external:
            dial_target = f"PJSIP/{rule.outbound_number}@voipms-endpoint"
            target_endpoint = None
        else:
            target_endpoint = rule.target_endpoint
            dial_target = f"PJSIP/{target_endpoint.endpoint_name}"
        return self._render_call_lines(
            f"exten => {rule.digits},1,",
            source_endpoint=rule.source_endpoint,
            target_endpoint=target_endpoint,
            dial_target=dial_target,
        )

    def _render_call_lines(
        self,
        first_prefix,
        source_endpoint=None,
        target_endpoint=None,
        dial_target=None,
    ):
        if dial_target is None:
            dial_target = f"PJSIP/{target_endpoint.endpoint_name}"

        blackout_checks = self._call_blackout_checks(source_endpoint, target_endpoint)
        applications = blackout_checks + [
            f"Dial({dial_target},30)",
            "Hangup()",
        ]
        return (
            [f"{first_prefix}{applications[0]}"]
            + [f" same => n,{application}" for application in applications[1:]]
            + [""]
        )

    def _call_blackout_checks(self, source_endpoint=None, target_endpoint=None):
        windows = []
        endpoint_pairs = (
            (source_endpoint, target_endpoint),
            (target_endpoint, source_endpoint),
        )
        for endpoint, other_endpoint in endpoint_pairs:
            if endpoint is None:
                continue
            if self._blackout_exception_applies(endpoint, other_endpoint):
                continue
            windows.extend(endpoint.blackout_windows)
        return [
            (
                f"GotoIfTime({window.time_range},{window.days},*,*?"
                "frontporch-blackout,s,1)"
            )
            for window in windows
        ]

    def _blackout_exception_applies(self, endpoint, other_endpoint):
        if not endpoint.blackout_windows or other_endpoint is None:
            return False
        return (
            endpoint.owner_type == "child"
            and endpoint.family_id == other_endpoint.family_id
            and other_endpoint.owner_type in {"parent", "family"}
        )

    def _render_blackout_context(self):
        return [
            "[frontporch-blackout]",
            "exten => s,1,NoOp(Rejecting call during child blackout period)",
            " same => n,Hangup(21)",
            "",
        ]

    def _render_public_inbound_context(self, configuration):
        inbound_context_name = "frontporch-public-inbound"
        if configuration.public_inbound_numbers:
            inbound_context_name = configuration.public_inbound_numbers[
                0
            ].inbound_context_name

        inbound_rules_by_public_number_id = {}
        for rule in configuration.inbound_external_caller_rules:
            inbound_rules_by_public_number_id.setdefault(
                rule.public_phone_number_id,
                [],
            ).append(rule)

        lines = [f"[{inbound_context_name}]"]
        restricted_contexts = []

        for public_number in configuration.public_inbound_numbers:
            canonical = public_number.canonical_extension
            caller_groups = {}
            for rule in inbound_rules_by_public_number_id.get(
                public_number.public_phone_number_id,
                (),
            ):
                caller_groups.setdefault(rule.caller_normalized_number, []).append(rule)

            lines.extend(
                [
                    (
                        f"exten => {canonical},1,"
                        f"NoOp(FrontPorch public inbound for {public_number.normalized_number})"
                    ),
                ]
            )
            approved_branches = []
            for rule_index, (caller_number, caller_rules) in enumerate(
                sorted(caller_groups.items()),
                start=1,
            ):
                caller_rules = sorted(
                    caller_rules,
                    key=lambda rule: (
                        rule.target_endpoint.extension,
                        rule.target_endpoint.device_id,
                    ),
                )
                if len(caller_rules) == 1:
                    label = f"approved-{canonical}-{rule_index}"
                    destination = label
                    target_endpoint = caller_rules[0].target_endpoint
                    approved_branches.extend(
                        self._render_labeled_target_call(label, target_endpoint)
                    )
                else:
                    context_name = (
                        f"frontporch-inbound-{public_number.public_phone_number_id}"
                        f"-{rule_index}"
                    )
                    destination = f"{context_name},s,1"
                    restricted_contexts.append((context_name, caller_number, caller_rules))

                for caller_id in caller_rules[0].caller_id_variants:
                    lines.append(
                        (
                            ' same => n,GotoIf($["${CALLERID(num)}" = '
                            f'"{caller_id}"]?{destination})'
                        )
                    )
            lines.extend(
                [
                    (
                        " same => n,NoOp(Rejecting unapproved public inbound caller "
                        "${CALLERID(num)})"
                    ),
                    " same => n,Hangup(21)",
                    "",
                ]
            )
            lines.extend(approved_branches)
            if approved_branches:
                lines.append("")

            for extension in public_number.dialplan_extensions:
                if extension == canonical:
                    continue
                lines.extend(
                    [
                        (
                            f"exten => {extension},1,"
                            f"Goto({inbound_context_name},{canonical},1)"
                        ),
                        "",
                    ]
                )

        lines.extend(
            [
                "exten => _X!,1,NoOp(Rejecting unmatched public inbound destination ${EXTEN})",
                " same => n,Hangup(21)",
                "",
            ]
        )

        for context_name, caller_number, caller_rules in restricted_contexts:
            lines.extend(
                [
                    f"[{context_name}]",
                    (
                        f"exten => s,1,NoOp(FrontPorch restricted inbound caller "
                        f"{caller_number})"
                    ),
                    " same => n,Answer()",
                    " same => n,WaitExten(10)",
                    " same => n,Hangup(21)",
                    "",
                ]
            )
            for rule in caller_rules:
                target = rule.target_endpoint
                lines.extend(
                    self._render_call_lines(
                        f"exten => {target.extension},1,",
                        target_endpoint=target,
                    )
                )
            lines.extend(
                [
                    "exten => i,1,Hangup(21)",
                    "exten => t,1,Hangup(21)",
                    "exten => _X!,1,Hangup(21)",
                    "",
                ]
            )

        return lines

    def _render_labeled_target_call(self, label, target_endpoint):
        return self._render_call_lines(
            f" same => n({label}),",
            target_endpoint=target_endpoint,
        )

    def render_files(self, configuration):
        return {
            PJSIP_FILENAME: self.render_pjsip(configuration),
            EXTENSIONS_FILENAME: self.render_extensions(configuration),
        }

    def write_files(self, configuration, output_dir):
        output_path = Path(output_dir)
        output_path.mkdir(parents=True, exist_ok=True)

        written = []
        for filename, content in self.render_files(configuration).items():
            destination = output_path / filename
            _atomic_write(destination, content)
            written.append(destination)
        return tuple(written)


def _atomic_write(destination, content):
    destination = Path(destination)
    with NamedTemporaryFile(
        "w",
        encoding="utf-8",
        dir=destination.parent,
        delete=False,
    ) as temporary:
        temporary.write(content)
        temporary_path = Path(temporary.name)

    os.chmod(temporary_path, 0o644)
    os.replace(temporary_path, destination)
